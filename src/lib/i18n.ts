export function translateText(value: string, locale: Locale) {
  const exact = clientTextTranslations(locale)[value];
  if (exact) return exact;

  const patterns = locale === "en" ? englishPatternTranslations : turkishPatternTranslations;
  for (const [pattern, replacer] of patterns) {
    const match = value.match(pattern);
    if (match) return replacer(...match);
  }

  return value;
}

export function clientTextTranslations(locale: Locale) {
  return {
    ...statusLabels[locale],
    ...displayTextTranslations(locale),
    ...(locale === "en"
      ? { ...englishPhraseTranslations, ...additionalEnglishPhraseTranslations }
      : {
          ...invertTranslations(englishPhraseTranslations),
          ...invertTranslations(additionalEnglishPhraseTranslations),
          ...turkishPhraseTranslations
        })
  };
}
